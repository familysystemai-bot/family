def _normalize(text: str) -> str:
    """تطبيع النص العربي للمقارنة: إزالة الهمزات والتشكيل وتوحيد الحروف."""
    t = (text or "").strip()
    # إزالة التشكيل (حركات)
    t = re.sub(r"[\u064B-\u065F\u0670]", "", t)
    # توحيد الألف
    t = re.sub(r"[أإآا]", "ا", t)
    # توحيد التاء المربوطة والهاء في كل مكان (ليس فقط نهاية الكلمة)
    t = t.replace("ة", "ه")
    # توحيد الألف المقصورة مع الياء في كل مكان
    t = t.replace("ى", "ي")
    return t.lower()


def _tokenize(text: str) -> list[str]:
    """يقسّم النص إلى كلمات بعد التطبيع."""
    return _normalize(text).split()


def infer_category_from_text(
    query: str,
    db_categories: list[dict] | None = None,
) -> Optional[str]:
    """
    يستنتج الفئة التي ينتمي إليها المنتج المطلوب.

    المنطق:
    1. ابحث في أسماء الأقسام الفعلية من قاعدة البيانات (db_categories).
       كل قسم فيه: {name: str, ...}
    2. إذا لم تجد → استخدم القاموس المرجعي (_KEYWORD_CATEGORY_MAP).

    يعيد اسم الفئة كنص عربي، أو None إذا لا يوجد تطابق.
    """
    if not query:
        return None

    q_norm = _normalize(query)
    q_tokens = set(_tokenize(query))

    # ── جدول مرادفات مفرد/جمع للمقارنة ──
    _SYNONYMS = {
        "عبايه": "عبايات", "عبايات": "عبايه",
        "فستان": "فساتين", "فساتين": "فستان",
        "قميص": "قمصان", "قمصان": "قميص",
        "بنطلون": "بناطيل", "بناطيل": "بنطلون",
        "سروال": "سراويل", "سراويل": "سروال",
        "حذاء": "احذيه", "احذيه": "حذاء",
        "شنطه": "شنط", "شنط": "شنطه",
        "حقيبه": "حقائب", "حقائب": "حقيبه",
        "ساعه": "ساعات", "ساعات": "ساعه",
        "عطر": "عطور", "عطور": "عطر",
        "ستاره": "ستائر", "ستائر": "ستاره",
        "سجاده": "سجاد", "سجاد": "سجاده",
        "وساده": "وسائد", "وسائد": "وساده",
        "بطانيه": "بطانيات", "بطانيات": "بطانيه",
        "ثوب": "اثواب", "اثواب": "ثوب",
        "شماغ": "اشمغه", "اشمغه": "شماغ",
    }

    # توسّع الاستعلام بالمرادفات
    q_tokens_expanded = set(q_tokens)
    for tok in list(q_tokens):
        syn = _SYNONYMS.get(tok)
        if syn:
            q_tokens_expanded.add(_normalize(syn))

    # ── الخطوة 1: مقارنة مع أقسام قاعدة البيانات الفعلية ──
    if db_categories:
        best_match: Optional[str] = None
        best_score = 0
        for cat in db_categories:
            cat_name = (cat.get("name") or cat.get("category_name") or "").strip()
            if not cat_name:
                continue
            cat_norm = _normalize(cat_name)
            cat_tokens = set(_tokenize(cat_name))

            # نقاط للكلمات المشتركة (مع المرادفات)
            shared = q_tokens_expanded & cat_tokens
            score = len(shared)

            # نقطة إضافية إذا اسم الفئة موجود كنص فرعي في الاستعلام
            if cat_norm in q_norm or q_norm in cat_norm:
                score += 2

            if score > best_score:
                best_score = score
                best_match = cat_name

        if best_score > 0 and best_match:
            return best_match

    # ── الخطوة 2: القاموس المرجعي ──
    for keyword, category in _KEYWORD_CATEGORY_MAP.items():
        kw_norm = _normalize(keyword)
        if kw_norm in q_norm:
            return category

    return None


