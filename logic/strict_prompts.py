def build_orchestrator_system_prompt(
    database_context: Optional[Dict[str, Any]] = None,
) -> str:
    """
    System prompt للمنسّق الرئيسي (run_chat_orchestrator).
    يحقن البيانات الفعلية + القواعد الصارمة.
    """
    base = (
        "أنتِ منسّقة محادثة لخدمة عملاء مجمع العائلة. مهمتكِ: فهم الطلب، اختيار الإجراء المناسب، "
        "وصياغة رسالة قصيرة ومباشرة بأسلوب سعودي لبق."
    )

    ctx_block = ""
    if database_context:
        try:
            ctx_block = (
                "\n\n📊 السياق المتاح (استخدمه فقط):\n"
                + json.dumps(database_context, ensure_ascii=False, indent=2)[:4200]
            )
        except Exception:
            ctx_block = ""

    return (
        f"{base}\n{CUSTOMER_SERVICE_PERSONA}\n{STRICT_RULES_BLOCK}"
        f"{ORCHESTRATOR_EXTRA_RULES}{ctx_block}"
    )


def build_recommendation_system_prompt() -> str:
    """System prompt لتوليد رسالة توصية منتجات."""
    base = (
        "أنت كاتب رسائل خدمة عملاء لمجمع العائلة.\n"
        "وظيفتك: إعادة صياغة رسالة عرض منتجات بشكل ودود.\n"
        "اقتصر على المنتجات في 'product_names_shown' فقط.\n"
        "لا تخترع منتجات. لا تذكر أسعاراً. لا تَعد بعروض."
    )
    return f"{base}\n{STRICT_RULES_BLOCK}"


def build_search_analysis_system_prompt() -> str:
    """System prompt لاستخراج حقول البحث (presearch)."""
    base = (
        "أنت محلّل نصوص لمجمع العائلة.\n"
        "وظيفتك: استخراج حقول البحث من رسالة العميل (gender, occasion, target, style, color).\n"
        "ارجع JSON فقط. لا تضف شيئاً غير الحقول المستخرجة.\n"
        "إذا لم تجد قيمة لحقل → اتركه null."
    )
    return f"{base}\n{STRICT_RULES_BLOCK}"


